<article class="flex flex-col gap-4 mt-[10vh] p-4 max-w-prose mx-auto text-justify leading-relaxed">
  <header class="contents">
    <h1 class="text-4xl leading-loose font-bold font-serif">Welcome to <code>@colibri-hq/ui</code>!</h1>
    <strong class="font-medium">
      This package contains the UI components for the Colibri project, and is only intended for usage within Colibri
      itself.
      Therefore, there are no demo routes or examples available&nbsp;here.
    </strong>
  </header>
  <p>
    <strong class="font-semibold">However</strong>, all components are documented in
    <a class="underline decoration-2" rel="noopener nofollow" href="https://storybook.js.org/">Storybook</a>, a tool for
    developing UI components in isolation. Storybook allows you to view the components in different states, interact
    with them, and see how they behave with various inputs. It also provides a way to document the components and
    their usage.
  </p>
  <p>
    To view the Colibri UI components in Storybook, you can run the following&nbsp;command:
  </p>
  <pre
    class="bg-gradient-to-br from-blue-50 dark:from-blue-950 to-blue-100/75 dark:to-blue-950/50 rounded-md p-4
    ring ring-blue-500 dark:ring-blue-700 shadow shadow-blue-500/10 text-blue-950 dark:text-blue-100
    select-all"
  ><code>pnpm run storybook</code></pre>
  <p>
    This will start the Storybook server, and you can view the components in your browser at
    <a class="underline decoration-2" href="http://localhost:6006"><code>localhost:6006</code></a>.
  </p>
</article>
